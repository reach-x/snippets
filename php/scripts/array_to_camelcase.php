function convert_array_to_camel_case($array)
{
	$new_array = [];

	foreach ($array as $key => $value) {
		if (is_array($value)) {
			if (empty($key)) {
				$new_array[] = convert_array_to_camel_case($value);
			} else {
				$new_array[from_camel_case($key)][] = convert_array_to_camel_case($value);
			}
		} else {
			$new_array[from_camel_case($key)] = $value;
		}
	}
	return $new_array;
}

function from_camel_case($input)
{
	preg_match_all('!([A-Z][A-Z0-9]*(?=$|[A-Z][a-z0-9])|[A-Za-z][a-z0-9]+)!', $input, $matches);
	$ret = $matches[0];
	foreach ($ret as &$match) {
		$match = $match == strtoupper($match) ? strtolower($match) : lcfirst($match);
	}
	return implode('_', $ret);
}

